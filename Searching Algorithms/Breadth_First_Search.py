# Breadth First Search (BFS) is a graph traversal algorithm that explores a graph level by level, starting from a chosen vertex.
# It visits all the neighbors of the starting vertex before moving on to their neighbors, ensuring that nodes at the same level are visited
# before deeper levels. BFS is often used to find the shortest path between two vertices and to explore all reachable nodes in a graph.

# Big O Notation of Breadth First Search:
# O(V + E)
# V represents the number of vertices and E represents the number of edges in the graph.